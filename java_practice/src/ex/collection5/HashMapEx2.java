package ex.collection5;
import java.util.*;

public class HashMapEx2 {

	public static void main(String[] args) {
		HashMap map = new HashMap();
		map.put("kim", new Integer(90));    //key중복 제거
		map.put("kim", new Integer(100));
		map.put("lee", new Integer(100));
		map.put("kang", new Integer(80));
		map.put("chae", new Integer(90));
		
		Set set = map.entrySet();  // 엔트리 형태로 set에 저장하여 반환
		Iterator it = set.iterator();
		
		while(it.hasNext()) {
			Map.Entry e = (Map.Entry) it.next();
			System.out.println("이름 : " + e.getKey() + ", 점수 : " + e.getValue());
			
		}
        set = map.keySet(); // 모든 키가 저장된 set을 반환
        System.out.println("참가자 명단 : " + set);
        
        Collection values = map.values();
        it = values.iterator();
        
        int total = 0;
        while(it.hasNext()) {
        	Integer i = (Integer)it.next();
        	total += i.intValue();
        }
        
        System.out.println("총점 : " + total);
        System.out.println("평균 : " + (float)total/set.size());
        System.out.println("최고점수 : " + Collections.max(values));
        System.out.println("최저점수 : " + Collections.min(values));
	}

}
