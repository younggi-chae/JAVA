package ex.operator;

public class operatorEx3 {

	public static void main(String[] args) {
		int a = 10;
		int b = 4;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);// 2.5가 아닌 2를 출력, 데이터타입이 int이기 때문
		System.out.println(a%b); //나머지를 구할 때 %연산자 사용
		
	}

}
