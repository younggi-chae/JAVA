package ex.I_O3;
import java.io.*;
import java.text.SimpleDateFormat;
import java.util.*;

public class FileEx4 {

	public static void main(String[] args) {
		
		String currDir = System.getProperty("user.dir"); // 현재위치 정보를 얻는다.
		File dir = new File(currDir);
		
		File [] files = dir.listFiles();
		
		for(int i = 0; i < files.length; i++) {
			File f = files[i];
			String name = f.getName();
			SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mma");
			String attribute = "";
			String size = "";
			
			if(files[i].isDirectory()) {
				attribute = "DIR";
			}else {
				size = f.length() + "";
				attribute = f.canRead() ? "R" : " ";  // 읽을 수 있는 파일인지 확인
				attribute = f.canWrite() ? "W" : " "; // 쓸 수 있는 파일인지 확인
				attribute = f.isHidden() ? "H" : " "; // 숨김속성이 있는 파일인지 확인
			}
			
			System.out.printf("%s %3s %6s %s\n", df.format(new Date(f.lastModified())), attribute, size, name);
			
		}

	}

}
